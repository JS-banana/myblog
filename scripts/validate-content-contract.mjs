import { promises as fs } from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const postsDir = path.join(root, 'src/content/posts');
const slugCachePath = path.join(root, 'src/content/.slug-cache.json');
const redirectsPath = path.join(root, 'src/content/.redirects.json');
const generatedMarker = '<!-- Generated by sync-posts.mjs. Do not edit directly. -->';

async function readJson(filePath) {
  const content = await fs.readFile(filePath, 'utf8');
  return JSON.parse(content);
}

async function listMarkdownFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        return listMarkdownFiles(fullPath);
      }

      if (entry.isFile() && entry.name.endsWith('.md')) {
        return [fullPath];
      }

      return [];
    }),
  );

  return files.flat();
}

function assert(condition, message) {
  if (!condition) {
    throw new Error(message);
  }
}

function isRecord(value) {
  return typeof value === 'object' && value !== null && !Array.isArray(value);
}

async function validateSlugCache() {
  const slugCache = await readJson(slugCachePath);
  assert(isRecord(slugCache), '.slug-cache.json must be a JSON object');

  for (const [sourcePath, entry] of Object.entries(slugCache)) {
    assert(typeof sourcePath === 'string' && sourcePath.length > 0, 'Slug cache keys must be non-empty strings');
    assert(isRecord(entry), `Slug cache entry for "${sourcePath}" must be an object`);
    assert(typeof entry.slug === 'string' && entry.slug.length > 0, `Slug cache entry for "${sourcePath}" is missing "slug"`);
    assert(typeof entry.title === 'string' && entry.title.length > 0, `Slug cache entry for "${sourcePath}" is missing "title"`);
    assert(typeof entry.year === 'string' && /^\d{4}$/.test(entry.year), `Slug cache entry for "${sourcePath}" must include a 4-digit "year"`);
  }

  return slugCache;
}

async function validateRedirects() {
  const redirects = await readJson(redirectsPath);
  assert(isRecord(redirects), '.redirects.json must be a JSON object');

  for (const [from, to] of Object.entries(redirects)) {
    assert(from.startsWith('/'), `Redirect source "${from}" must start with "/"`);
    assert(typeof to === 'string' && to.startsWith('/'), `Redirect target for "${from}" must start with "/"`);
  }
}

async function validateGeneratedPosts(slugCache) {
  const markdownFiles = await listMarkdownFiles(postsDir);
  assert(markdownFiles.length > 0, 'At least one generated post fixture must exist under src/content/posts');

  for (const filePath of markdownFiles) {
    const content = await fs.readFile(filePath, 'utf8');
    const relativePath = path.relative(root, filePath);

    assert(content.includes(generatedMarker), `${relativePath} must include the generated-file marker comment`);
    assert(content.includes('sourcePath:'), `${relativePath} must include sourcePath in frontmatter`);

    const sourcePathLine = content
      .split('\n')
      .find((line) => line.trimStart().startsWith('sourcePath:'));

    if (sourcePathLine) {
      const sourcePath = sourcePathLine.split(':').slice(1).join(':').trim();
      assert(
        sourcePath in slugCache,
        `${relativePath} sourcePath "${sourcePath}" must exist in .slug-cache.json`,
      );
    }
  }
}

async function main() {
  const slugCache = await validateSlugCache();
  await validateRedirects();
  await validateGeneratedPosts(slugCache);

  console.log('Content contract validation passed.');
}

main().catch((error) => {
  console.error(error.message);
  process.exitCode = 1;
});
